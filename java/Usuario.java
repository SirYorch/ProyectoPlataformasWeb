public class Usuario{
    String uid;
    String stat;
    String nombre;
    String cedula;
    String telefono;
    String placa;
    String direccion;

    
    public Usuario(String uid, String stat, String nombre, String cedula,String telefono,String placa,String direccion){
        this.uid = uid;
        this.stat = stat;
        this.nombre= nombre; 
        this.cedula=cedula;
        this.telefono = telefono;
        this.placa = placa;
        this.direccion = direccion;
    }
    
}
